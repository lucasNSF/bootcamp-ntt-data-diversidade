// Laço de repetição FOR
for (let i = 0; i < 10; i++) {
  console.log(i);
}

// laço de repetição WHILE
let j = 10;
while (j >= 0) {
  console.log(j);
  j--;
}

// Laço de repetição FOR ... OF
const meuNome = "Lucas";
for (let letra of meuNome) {
  console.log(letra);
}

